package com.orion.java.hw6.taskA;

public class Animal {
    private String name;
    private String typeAnimal;

    public Animal(String name, String typeAnimal) {
        this.name = name;
        this.typeAnimal = typeAnimal;
    }

    public String getName() {
        return name;
    }

    public String getTypeAnimal() {
        return typeAnimal;
    }
}
